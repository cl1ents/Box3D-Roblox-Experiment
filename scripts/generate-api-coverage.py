#!/usr/bin/env python3
import argparse
import json
import re
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
MANIFEST = ROOT / "wasm" / "box3d_api.json"
HEADER_DIR = ROOT / "box3d" / "include" / "box3d"
OUT_JSON = ROOT / "build-wasm" / "box3d_api_coverage.json"
OUT_MD = ROOT / "build-wasm" / "box3d_api_coverage.md"

FUNCTION_RE = re.compile(r"\bB3_API\s+(.+?)\s+(b3[A-Za-z0-9_]+)\s*\(", re.DOTALL)

TOPIC_PREFIXES = [
    ("b3World", "World"),
    ("b3Body", "Body"),
    ("b3Shape", "Shape"),
    ("b3Joint", "Joint"),
    ("b3DistanceJoint", "DistanceJoint"),
    ("b3MotorJoint", "MotorJoint"),
    ("b3FilterJoint", "FilterJoint"),
    ("b3ParallelJoint", "ParallelJoint"),
    ("b3PrismaticJoint", "PrismaticJoint"),
    ("b3RevoluteJoint", "RevoluteJoint"),
    ("b3SphericalJoint", "SphericalJoint"),
    ("b3WeldJoint", "WeldJoint"),
    ("b3WheelJoint", "WheelJoint"),
    ("b3DynamicTree", "DynamicTree"),
    ("b3Character", "CharacterMover"),
    ("b3DebugDraw", "DebugDraw"),
]

NAME_TOPICS = [
    ("Contact", "Contact"),
    ("Sensor", "Events"),
    ("Event", "Events"),
    ("Query", "Query"),
    ("Ray", "Query"),
    ("Cast", "Query"),
    ("Hull", "ConvexHull"),
    ("Mesh", "TriangleMesh"),
    ("HeightField", "HeightField"),
    ("Capsule", "Capsule"),
    ("Sphere", "Sphere"),
    ("Compound", "Compound"),
    ("Collision", "ShapeCollision"),
    ("Distance", "ShapeCollision"),
    ("File", "Recording"),
    ("Record", "Recording"),
    ("Id", "Ids"),
    ("Transform", "Math"),
    ("Quat", "Math"),
    ("Matrix", "Math"),
    ("AABB", "Geometry"),
    ("Plane", "Geometry"),
]

STATUS_BY_TOPIC = {
    "DynamicTree": "unsupported",
    "DebugDraw": "callback-needed",
    "Recording": "file-io",
}


def clean_return_type(text: str) -> str:
    return " ".join(text.replace("\n", " ").split())


def classify_topic(header: str, name: str) -> str:
    for prefix, topic in TOPIC_PREFIXES:
        if name.startswith(prefix):
            return topic

    for part, topic in NAME_TOPICS:
        if part in name:
            return topic


    if header in {"base.h", "constants.h"}:
        return "Base"
    if header == "math_functions.h":
        return "Math"
    if header == "collision.h":
        return "Geometry"
    if header == "types.h" and name.startswith("b3Default"):
        return "Base"
    return "Base"


def default_status(topic: str) -> str:
    return STATUS_BY_TOPIC.get(topic, "bridge-needed")


def collect_header_functions() -> list[dict[str, str]]:
    functions = []
    for header in sorted(HEADER_DIR.glob("*.h")):
        text = header.read_text()
        text = re.sub(r"//.*", "", text)
        text = re.sub(r"^\s*#.*$", "", text, flags=re.MULTILINE)
        for match in FUNCTION_RE.finditer(text):
            return_type = clean_return_type(match.group(1))
            name = match.group(2)
            if name in {"b3SetAllocator", "b3SetAssertFcn", "b3SetLogFcn"}:
                status = "callback-needed"
            elif "File" in name:
                status = "file-io"
            else:
                status = default_status(classify_topic(header.name, name))

            functions.append(
                {
                    "name": name,
                    "returnType": return_type,
                    "header": str(header.relative_to(ROOT)),
                    "topic": classify_topic(header.name, name),
                    "status": status,
                }
            )
    return functions


def manifest_upstream(manifest: dict) -> dict[str, list[str]]:
    covered: dict[str, list[str]] = {}
    for fn in manifest.get("functions", []):
        bridge_name = fn["name"]
        for upstream in fn.get("upstream", []):
            covered.setdefault(upstream, []).append(bridge_name)
    return covered


def write_markdown(report: dict) -> None:
    lines = [
        "# Box3D API Coverage",
        "",
        "Generated by `scripts/generate-api-coverage.py`.",
        "",
        f"Total upstream APIs: {report['summary']['total']}",
        f"Implemented via manifest: {report['summary']['implemented']}",
        f"Unmapped upstream APIs: {report['summary']['unmapped']}",
        "",
        "## Topics",
        "",
    ]

    for topic, counts in sorted(report["topics"].items()):
        lines.append(f"- `{topic}`: {counts['total']} total, {counts.get('implemented', 0)} implemented")

    lines.extend(["", "## Unmapped APIs", ""])
    for fn in report["functions"]:
        if fn["status"] != "implemented":
            lines.append(f"- `{fn['name']}` ({fn['topic']}, {fn['status']})")

    OUT_MD.write_text("\n".join(lines) + "\n")


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate upstream Box3D API coverage report")
    parser.add_argument("--check", action="store_true", help="fail when any upstream API is unmapped")
    args = parser.parse_args()

    manifest = json.loads(MANIFEST.read_text())
    upstream_to_bridge = manifest_upstream(manifest)
    functions = collect_header_functions()

    topics: dict[str, dict[str, int]] = {topic: {"total": 0, "implemented": 0} for topic in manifest.get("documentationTopics", [])}
    implemented = 0
    for fn in functions:
        bridge_names = upstream_to_bridge.get(fn["name"])
        if bridge_names:
            fn["status"] = "implemented"
            fn["bridge"] = bridge_names
            implemented += 1

        topic_counts = topics.setdefault(fn["topic"], {"total": 0, "implemented": 0})
        topic_counts["total"] += 1
        if fn["status"] == "implemented":
            topic_counts["implemented"] += 1

    report = {
        "documentationTopics": manifest.get("documentationTopics", []),
        "summary": {
            "total": len(functions),
            "implemented": implemented,
            "unmapped": len(functions) - implemented,
        },
        "topics": topics,
        "functions": functions,
    }

    OUT_JSON.parent.mkdir(parents=True, exist_ok=True)
    OUT_JSON.write_text(json.dumps(report, indent=2) + "\n")
    write_markdown(report)

    if args.check and report["summary"]["unmapped"]:
        raise SystemExit(f"{report['summary']['unmapped']} upstream Box3D APIs are unmapped")


if __name__ == "__main__":
    main()
