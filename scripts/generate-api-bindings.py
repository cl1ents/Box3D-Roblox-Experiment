#!/usr/bin/env python3
import argparse
import json
import re
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
MANIFEST = ROOT / "wasm" / "box3d_api.json"
CMAKE_OUT = ROOT / "build-wasm" / "box3d_exports.cmake"
LUAU_OUT = ROOT / "src" / "shared" / "Box3D" / "Internal" / "Omitted.luau"
BRIDGE = ROOT / "wasm" / "box3d_bridge.c"


def normalize(name: str) -> str:
    return name[1:] if name.startswith("_") else name


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate Box3D wasm export bindings")
    parser.add_argument("--profile", default="demo")
    parser.add_argument("--omit", default="")
    args = parser.parse_args()

    manifest = json.loads(MANIFEST.read_text())
    functions = manifest["functions"]
    known = {fn["name"] for fn in functions}
    bridge_source = BRIDGE.read_text()
    for name in re.findall(r"^B3D_EXPORT\s+[^()]+\s+(b3d_[A-Za-z0-9_]+)\s*\(", bridge_source, re.MULTILINE):
        if name in known:
            continue
        functions.append({
            "name": name,
            "wasm": f"_{name}",
            "group": name.split("_")[1] if "_" in name else "bridge",
            "args": [],
            "returns": "unknown",
            "publicName": name,
            "reason": "full API bridge wrapper",
        })
        known.add(name)
    by_name = {fn["name"]: fn for fn in functions}
    profile = args.profile

    if profile not in manifest["profiles"]:
        raise SystemExit(f"unknown API profile: {profile}")

    profile_names = manifest["profiles"][profile]
    if profile_names == ["*"]:
        included = set(by_name)
    else:
        included = set(profile_names)

    omit = {normalize(part.strip()) for part in args.omit.split(";") if part.strip()}
    included -= omit

    unknown = sorted((included | omit) - set(by_name))
    if unknown:
        raise SystemExit("unknown API function(s): " + ", ".join(unknown))

    exports = [fn["wasm"] for fn in functions if fn["name"] in included]
    omitted = [fn for fn in functions if fn["name"] not in included]

    CMAKE_OUT.parent.mkdir(parents=True, exist_ok=True)
    quoted_exports = ",".join(f"'{name}'" for name in exports)
    CMAKE_OUT.write_text(
        "# Generated by scripts/generate-api-bindings.py\n"
        f"set(BOX3D_WASM_API_PROFILE \"{profile}\")\n"
        f"set(BOX3D_WASM_EXPORTED_FUNCTIONS \"[{quoted_exports}]\")\n"
    )

    LUAU_OUT.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "--!strict",
        "-- Generated by scripts/generate-api-bindings.py",
        "",
        "return {",
    ]
    for fn in omitted:
        public_name = fn.get("publicName", fn["name"])
        reason = fn.get("reason", "omitted from current wasm API profile")
        lines.append(f"\t{fn['name']} = {{ publicName = \"{public_name}\", reason = \"{reason}\" }},")
    lines.append("}")
    LUAU_OUT.write_text("\n".join(lines) + "\n")


if __name__ == "__main__":
    main()
